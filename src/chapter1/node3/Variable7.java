package chapter1.node3;

public class Variable7 {
    /*
    以;结尾的一段代码，即为一个表达式
     */

    public static void main(String[] args) {
        //每一句话都是一个表达式
        int i = 5;
        System.out.println(5);

        // ; 也是一个完整的表达式
        //一个空;也是一个表达式
        ;
        ;
        ;
        ;
    }

}
